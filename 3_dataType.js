console.log("boolean data type")
console.log(booleanTrue)
console.log(booleanFalse)


let isnull = null; //null data type은 변수의 값을 null로 할당
console.log("null data type")
console.log(isnull)


let undefinded; //undefinded는 변수에 아무런 값을 할당하지 않음
