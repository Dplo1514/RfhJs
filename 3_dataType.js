//원시형(primitive) 데이터타입 number , string , boolean , null , undefinded  
let number = 25; //number data type은 정수형 데이터
console.log("number data type")
console.log(number)

let name = "hyuk_98_plo" //str data type은 ""안에 값을 넣어 할당 숫자가 들어가더라도 ""안에 할당되면 strType
console.log("string data type")
console.log(name)



let booleanTrue = true; //boolean data type은 true , false 값을 가짐
